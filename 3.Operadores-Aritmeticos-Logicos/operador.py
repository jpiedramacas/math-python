# Operadores Aritméticos y Lógicos

# Operadores aritméticos
num1 = 10
num2 = 5

# Suma
suma = num1 + num2
print("Suma:", suma)

# Resta
resta = num1 - num2
print("Resta:", resta)

# Multiplicación
multiplicacion = num1 * num2
print("Multiplicación:", multiplicacion)

# División
division = num1 / num2
print("División:", division)

# Módulo
modulo = num1 % num2
print("Módulo:", modulo)

# Potencia
potencia = num1 ** num2
print("Potencia:", potencia)

# Operadores lógicos
bool1 = True
bool2 = False

# AND lógico
resultado_and = bool1 and bool2
print("AND lógico:", resultado_and)

# OR lógico
resultado_or = bool1 or bool2
print("OR lógico:", resultado_or)

# NOT lógico
resultado_not = not bool1
print("NOT lógico:", resultado_not)

